// Get today's word based on date (consistent across all users)
export function getDailyWord(): string {
  const now = new Date();
  // Create a date string that changes at midnight local time
  const dateKey = `${now.getFullYear()}-${now.getMonth()}-${now.getDate()}`;

  // Simple hash of the date string
  let hash = 0;
  for (let i = 0; i < dateKey.length; i++) {
    const char = dateKey.charCodeAt(i);
    hash = ((hash << 5) - hash) + char;
    hash |= 0;
  }

  const index = Math.abs(hash) % WORD_LIST.length;
  return WORD_LIST[index];
}

// Get the date key for storage
export function getDateKey(): string {
  const now = new Date();
  return `${now.getFullYear()}-${String(now.getMonth() + 1).padStart(2, '0')}-${String(now.getDate()).padStart(2, '0')}`;
}

// Check if a guess is a valid 5-letter word
export function isValidWord(guess: string): boolean {
  if (guess.length !== 5) return false;
  return WORD_LIST.includes(guess.toUpperCase());
}

// Get tomorrow's midnight for countdown
export function getNextMidnight(): Date {
  const now = new Date();
  const tomorrow = new Date(now);
  tomorrow.setDate(tomorrow.getDate() + 1);
  tomorrow.setHours(0, 0, 0, 0);
  return tomorrow;
}

// Format time remaining until next word
export function formatTimeRemaining(ms: number): string {
  const hours = Math.floor(ms / (1000 * 60 * 60));
  const minutes = Math.floor((ms % (1000 * 60 * 60)) / (1000 * 60));
  const seconds = Math.floor((ms % (1000 * 60)) / 1000);
  return `${String(hours).padStart(2, '0')}:${String(minutes).padStart(2, '0')}:${String(seconds).padStart(2, '0')}`;
}
